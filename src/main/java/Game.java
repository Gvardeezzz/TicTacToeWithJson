import java.util.ArrayList;

public class Game {
    private ArrayList<Step> steps;

    public Game(){
        this.steps = steps;
    }

    public Game(ArrayList<Step> steps) {
        this.steps = steps;
    }

    public ArrayList<Step> getSteps() {
        return steps;
    }

    public void setSteps(ArrayList<Step> steps) {
        this.steps = steps;
    }
}
